                    <p>
                        <b>Effective 03/23/2009</b><br>
                        Members of Purdue Pilots, Inc. (PPI) shall be governed by the 
                        following operating rules and these rules shall supersede and cancel
                        all previous rules of the club. For reference, please refer to the 
                        <a href="docs/bylaws.pdf">original bylaws</a>.
                    </p>
                </div>
                
                <div class="topic" id="memberDuties">
                    <h3 class="title">Member Duties</h3>
                    <ol>
                        <li>
                            Members of PPI shall observe all existing Federal Aviation 
                            Regulations, state and local airport rules, 
                            Purdue Aviation, LLC rules, and club rules (PPI Bylaws).
                        </li>
                        <li>
                            PPI aircraft shall not be flown by persons other than current
                            PPI members.
                        </li>
                        <li>PPI aircraft shall not be 
                            loaned or rented to non-members.
                        </li>
                        <li>
                            Use of club aircraft for commercial purposes, other than
                            instruction given to a member by an instructor approved by 
                            Purdue Aviation, LLC, is prohibited.
                        </li>
                        <li>It is the member's 
                            responsibility to report any aircraft 
                            squawks or damage. There is a form in the aircraft flight
                            book; give the original of the form to the Purdue
                            Aviation, LLC. front desk personnel.
                        </li>
                        <li>A member is grounded if he 
                            or she misses two (2) consecutive club business meetings 
                            without prior approval by the club president. The 
                            grounding is lifted when a member attends a club 
                            business meeting.
                        </li>
                    </ol>
                </div>
                
                <div class="topic" id="membership">
                    <h3 class="title">Membership</h3>
                    <ol>
                        <li>
                            New members shall pay $65 for a first-time club 
                            initiation fee.
                        </li>
                        <li>
                            Club dues thereafter shall be $50 per half-year; each 
                            half defined by Purdue Pilots, Inc.
                        </li>
                        <li>
                            No member shall be granted fractional or discounted 
                            initiation fees or regular club dues.
                        </li>
                    </ol>
                </div>
                
                <div class="topic" id="flightRules">
                    <h3 class="title">Flight Rules</h3>
                    <ol>
                        <li>
                            Pilot checkout and currency requirements are the same as 
                            those imposed by Purdue Aviation, LLC.
                        </li>
                        <li>
                            Pay as you fly. PPI does not extend credit to members.
                        </li>
                        <li>
                            Members should have their current membership card 
                            available to present to the front desk staff at Purdue
                            Aviation, Inc. prior to flight.
                        </li>
                        <li>
                            Smoking in PPI aircraft is prohibited.
                        </li>
                        <li>
                            The only maneuvers permitted are those required in a 
                            flight test for any airman's certificate.
                        </li>
                        <li>
                            Flight between the hours of sunset and sunrise shall be 
                            considered night flight (see also 
                            <a href="#nightReq">night requirements</a>).
                        </li>
                        <li>
                            A pilot shall occupy the left seat unless he has received
                            a check-out in the right seat by an approved instructor.
                        </li>
                        <li>
                            Early morning departures before 8:00 a.m. by pilots with
                            less than 100 hours are discouraged.
                        </li>
                        <li>
                            Any member who is more than 15 minutes late for his or 
                            her scheduled flight time shall forfeit the remainder of 
                            his period to any member desiring the aircraft.
                        </li>
                        <li>
                            The PPI Executive Board reserves the right to hold a 
                            prior hearing on all cross country flight requests. As a
                            guideline, the Executive Board will especially wish to 
                            evaluate the following:
                            <ul>
                                <li>
                                    Flights by members who have been active less 
                                    than one month and/or have less than 10 hours in
                                    PPI aircraft.
                                </li>
                                <li>
                                    Flights of over 200 nautical miles (nm) from 
                                    Lafayette when a member has less than 100 hours 
                                    total time or less than 30 hour of cross country
                                    time.
                                </li>
                                <li>
                                    Flights over 500nm from Lafayette.
                                </li>
                                <li>
                                    Flights involving over 72 consecutive hours' use
                                    of PPI aircraft.
                                </li>
                            </ul>
                        </li>
                        <li>
                            If a pilot is called before a PPI Safety Committee, he 
                            may fly only with an instructor pending the findings of
                            the Safety Committee.
                        </li>
                    </ol>
                </div>
                
                <div class="topic" id="allXC">
                    <h3 class="title">All Cross Country Flights</h3>
                    
                    <ol>
                        <li>
                            A flight plan shall be left with airport front desk staff
                            if the cross country flight is over 50nm from Lafayette.
                        </li>
                        <li>
                            A flight plan shall be filed with the FAA if a flight 
                            is over 100nm.
                        </li>
                        <li>
                            PPI aircraft shall be landed only at public airports, 
                            unless prior approval is obtained from the Executive Board.
                        </li>
                        <li>
                            Members shall be reimbursed for oil and routine service.
                            They will be reimbursed for fuel purchases at the current
                            fuel reimbursement rate determined by the Treasurer. This
                            rate will be based on the fuel price at Purdue Aviation
                            on or near the date of fuel purchase. Members must present
                            a Request for Check from and an original receipt to the 
                            Treasurer to receive reimbursement.
                        </li>
                        <li>
                            When other than routine maintenance is encountered on a 
                            cross country flight, advice should be obtained from 
                            Purdue Aviation, LLC or the PPI Executive Board 
                            before any repairs shall be authorized.
                        </li>
                        <li>
                            When weathered-in, call Purdue Aviation or the 
                            PPI president.
                        </li>
                        <li>
                            Over-water flights beyond gliding distance from land 
                            must be approved in advance by the PPI Executive Board
                            on an individual basis, after ascertaining that sufficient
                            emergency equipment and insurance coverage has been provided.
                        </li>
                    </ol>
                </div>
                
                <div class="topic" id="multiDayXC">
                    <h3 class="title">Multi-Day Cross Country Flights</h3>
                    <ol>
                        <li>
                            All flights of over 24 hours, or overnight, require
                            prior Executive Board approval.
                        </li>
                        <li>
                            The club president or designee and any two PPI Executive
                            Board members may approve requests for multi-day cross
                            country flights.
                        </li>
                        <li>
                            A member shall guarantee PPI two hours of flight time 
                            for each consecutive 24 hours of aircraft use.
                        </li>
                        <li>
                            A pilot who is grounded by weather or maintenance at a 
                            distant location will not be expected to meet the 
                            minimum flight time per day requirement. However, if the
                            aircraft is left at that location, the pilot will be 
                            responsible for the expenses of returning the aircraft
                            to the Lafayette Airport.
                        </li>
                        <li>
                            Pilots who have at least 190 hours of total time, hold
                            a current instrument rating, have 1 calendar year membership
                            in PPI, have made at least 3 multiple day cross country 
                            flights in PPI aircraft, and have 20 hours of multiple 
                            day cross country time in PPI aircraft may apply to the
                            Executive Board for an exemption to rule #1 for multiple
                            day cross country flights. This exemption shall remain
                            in force for 1 calendar year or until specifically revoked
                            by the Executive Board.
                        </li>
                    </ol>
                </div>
                
                <div class="topic" id="nightReq">
                    <h3 class="title">Night Requirements</h3>
                    <ol>
                        <li>
                            A member shall have 100 hours total flight time which 
                            must include 5 hours instrument time and 40 hours as a 
                            Private Pilot.
                        </li>
                        <li>
                            A night check-out by an authorized club instructor 
                            shall be required.
                        </li>
                        <li>
                            Night flight shall be restricted to 25 nm from 
                            Lafayette except when accompanied by an instructor.
                        </li>
                        <li>
                            Night flight is unrestricted if the pilot is instrument 
                            rated, checked-out at night, and the aircraft certified 
                            for instrument flight.
                        </li>
                        <li>
                            It is recommended that a flight plan be filed before 
                            flying at night.
                        </li>
                    </ol>
                </div>
                
                <div class="topic" id="credits">
                    <h3 class="title">Earning Flight Time</h3>
                    <ol>
                        <li>
                            Credit of one hour of flight time shall be allowed for 
                            aircraft washed when approved by the President and 
                            Maintenance Officer.
                        </li>
                        <li>
                            Credit of one-half hour of flight time shall be allowed 
                            for aircraft waxing when approved by the President and 
                            Maintenance Officer.
                        </li>
                        <li>
                            Members must be able to legally work in the United 
                            States to receive credit for washing/waxing an aircraft.
                            A Request for Check form must be completed and presented
                            to the Treasurer. Credit for washing/waxing an aircraft 
                            is based up on the current hourly rate for the cheapest 
                            aircraft in the fleet. This credit can be divided among 
                            members, but it is the members' responsibility to do so.
                        </li>
                    </ol>
                </div>
            </div>
            
            <div class="topic" id="elections">
                <h3 class="title">Club Officers and Elections</h3>
                <ol>
                    <li>
                        Members of PPI shall elect a President, Vice President, 
                        Treasurer, Secretary, Public Relations Officer, 
                        Maintenance Officer, and Faculty Advisor(s) at the club's
                        annual business meeting which shall take place toward the 
                        end of the Fall semester.
                    </li>
                    <li>
                        The PPI Executive Board shall be comprised of the 
                        President, Vice President, Treasurer, Secretary, 
                        Public Relations Officer, Maintenance Officer, and the 
                        Faculty Advisor(s). The Executive Board shall be 
                        responsible for the management of the club, and the 
                        President and Faculty Advisor(s) shall have the final 
                        authority on major club decisions.
                    </li>
                    <li>
                        To become an officer, a member must be in good standing 
                        with the club and announce his or her candidacy at or 
                        before the start of the annual business meeting. 
                        Presidential candidates must have been an active PPI 
                        member for at least nine months prior to officer elections.
                    </li>
                    <li>
                        Members may run for multiple officer positions, however, 
                        they may only hold one officer position at a time.
                    </li>
                    <li>
                        Officer elections will be managed by either the 
                        Secretary or the Faculty Advisor.
                    </li>
                    <li>
                        The President shall oversee all aspects of club business,
                        organize and run all bi-monthly club meetings, affect 
                        fair and logical changes to club policy when necessary, 
                        and advance the club agenda(s). The President shall also 
                        preside over and lead meeting of the Executive Board.
                    </li>
                    <li>
                        The Vice President shall stand in for the President 
                        should he or she be unable to attend a club meeting, act 
                        as a liaison to the Aviation Student Organization 
                        Roundtable, and assume the role of President should the 
                        current President resign or otherwise be unable to 
                        fulfill the responsibilities of the role.
                    </li>
                    <li>
                        The Treasurer shall be responsible for the collection of
                        dues and initiation fees, issuing PPI membership cards, 
                        depositing receipts with the Purdue Bursar's Office, 
                        receiving monthly flight fees and statements from 
                        Purdue Aviation and Lafayette Avionics, preparing 
                        check request forms for the Business Office for Student 
                        Organizations, keeping club financial and membership 
                        spreadsheets up to date, setting up aircraft and 
                        liability insurance in early January, preparing Federal 
                        and State tax forms in October, and processing any other
                        miscellaneous club expenses throughout the year. The 
                        Treasurer shall deliver a report on the club's current 
                        financial status at every meeting.
                    </li>
                    <li>
                        The Secretary shall take attendance at each PPI meeting,
                        organize and maintain a record of club attendance, 
                        take notes at all meetings, distribute meeting minutes 
                        to all members, mange the club's e-mail lists, and 
                        maintain a club roster with the Treasurer.
                    </li>
                    <li>
                        The Public Relations Officer shall be responsible for 
                        enhancing the club's publicity and advertising, 
                        organizing call-outs at the beginning of the Fall and 
                        Spring semesters, and creating and distributing any 
                        advertisement materials including flyers and posters.
                    </li>
                    <li>
                        The Maintenance Officer shall keep track of any 
                        maintenance performed on the club's aircraft, inform the 
                        membership of said maintenance, and help organize and 
                        expedite major maintenance and repairs on club aircraft. 
                        The Maintenance Officer shall deliver a report on the 
                        aircraft's current status and record any new aircraft 
                        squawks at every meeting.
                    </li>
                    <li>
                        The Faculty Advisor shall provide advice and guidance to 
                        the president and executive board on issues regarding 
                        club operation, act as a liaison with university 
                        administration on matters involving the standing and 
                        operation of the club, provide oversight of the financial
                        operation of the club, and serve as a mechanism for 
                        continuity in leadership and policies from year to year.
                    </li>
                    <li>
                        The webmaster shall be responsible for updating and 
                        maintaining the club's website and working with the 
                        Public Relations officer or President at times to 
                        disseminate information to PPI members via the website. 
                        This position shall be appointed by the President.
                    </li>
                    <li>
                        Officers are expected to attend every meeting unless 
                        prior arrangements are made with the President. If the 
                        President cannot attend a meeting, he or she shall make 
                        arrangements with the rest of the Executive Board.
                    </li>
                </ol>
            </div>
            
            <div class="topic" id="violations">
                <h3 class="title">Violations</h3>
                
                <ol>
                    <li>
                        Any violations of these rules and regulations of Purdue 
                        Pilots, Inc. shall be subject to review by the PPI Safety 
                        Committee.
                    </li>
                    <li>
                        Any unsafe operation or violation of any of the rules
                        by a member renders him liable either to temporary 
                        flight suspension, monetary fine, or both, pending a 
                        decision by the Executive Board.
                    </li>
                    <li>
                        Expulsion from the club will be recommended by the 
                        Executive Board in cases of extreme violation or recurring
                        unsafe operation.
                    </li>
                </ol>
            </div>
            
            <div class="topic" id="amendments">
                <h3 class="title">Changes and Amendments</h3>
                
                <ol>
                    <li>
                        Changes in, or amendments to these Operating Rules and 
                        Regulations may be effected only by the Executive Board.
                    </li>
                    <li>
                        Such changes shall become effective when incorporated 
                        into the minutes of an Executive Board meeting and 
                        notice being given to the membership by either posting 
                        to the PPImembers e-mail list or by reading the minutes 
                        at a general meeting of the membership.
                    </li>
                </ol>
